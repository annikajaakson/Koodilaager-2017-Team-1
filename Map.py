class map1:
	def __init__(self, resolution):
		self.x = 20
		self.y = 18
		self.resolution = resolution
	def draw(self, screen):
		rects = []
		for i in range(len(mapn)):
			width = self.resolution[0]/self.x
			height = self.resolution[1]/self.y
			if mapn[i] == 1:
				rects.append(pygame.Rect((i%self.x*width, math.floor(i/(self.y+2))*height, self.resolution[0]/self.x, self.resolution[1]/self.y)))
		for i in rects:
			pygame.draw.rect(screen, (160, 95, 49), i)
		return rects